#include "3-calc.h"

/**
 * op_add - function give add
 * @a:input1
 * @b:input2
 * Return: int
 **/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - give sub
 * @a:input1
 * @b:input2
 * Return: int
 **/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function give mul..
 * @a:input1
 * @b:input2
 * Return: int
 **/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function give div
 * @a:input1
 * @b:input2
 * Return: int
 **/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function give mod.
 * @a:input1
 * @b:input2
 * Return: int
 **/

int op_mod(int a, int b)
{
	return (a % b);
}
